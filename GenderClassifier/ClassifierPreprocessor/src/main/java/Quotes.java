public class Quotes {
    private String speaker;
    private String quote;
    private boolean iaAttributeTag;

    public String getSpeaker() {
        return speaker;
    }

    public void setSpeaker(String speaker) {
        this.speaker = speaker;
    }

    public String getQuote() {
        return quote;
    }

    public void setQuote(String quote) {
        this.quote = quote;
    }

    public boolean isIaAttributeTag() {
        return iaAttributeTag;
    }

    public void setIaAttributeTag(boolean iaAttributeTag) {
        this.iaAttributeTag = iaAttributeTag;
    }
}
